void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim){
	if(htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1){
		if(IsRisingEdge == 1){
			ICRisingValue = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
			IsRisingEdge = 0;
		}
		else if(IsRisingEdge == 0){
			ICFallingValue = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
			if(ICFallingValue > ICRisingValue){
				Difference = ICFallingValue - ICRisingValue;
			}
			else if(ICFallingValue < ICRisingValue){
				Difference = (0xFFFF-ICRisingValue)+ICFallingValue+1;
			}
			else{
				Error_Handler();
			}
			distanceBetweenVehicles = (float)Difference*COUNTS_TO_METERS;
			IsRisingEdge = 1;
		}
	}
}

